package practice;

public class Main01_method {
	public static void main(String[] args) {
		plus();
		minus();
		
		
	}
	
	public static void plus() {
		int x = 100;
		int y = 200;
		System.out.println(x + y);
	}
	
	public static void minus() {
		int x = 100;
		int y = 200;
		System.out.println(x - y);
	}
}
